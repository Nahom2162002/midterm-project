import math 

class Add:
    def add(x, y):
        return x + y

class Subtract:
    def subtract(x, y):
        return x - y 

class Multiply:
    def multiply(x, y):
        return x * y 

class Divide:
    def divide(x, y):
        return x / y 

class Root:
    def root(x):
        math.sqrt(x) 